    private void SignUpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpBtnActionPerformed
         // Retrieve input
    String fullName = fname.getText().trim();
    String email = emailAddress.getText().trim();
    String password = new String(pass.getPassword());

    // Validate inputs
    if (fullName.isEmpty()) {
        JOptionPane.showMessageDialog(new JFrame(), "Full Name is required", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (email.isEmpty()) {
        JOptionPane.showMessageDialog(new JFrame(), "Email Address is required", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    } else if (!isValidEmail(email)) {
        JOptionPane.showMessageDialog(new JFrame(), "Please enter a valid email address", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (password.isEmpty()) {
        JOptionPane.showMessageDialog(new JFrame(), "Password is required", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    } else if (!isValidPassword(password)) {
        JOptionPane.showMessageDialog(new JFrame(), "Password must be at least 8 characters long and contain a mix of letters, numbers, and special characters", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Insert into the database
    try {
        String SUrl = "jdbc:mysql://localhost:3306/java_user_database";
        String SUser = "root";
        String SPass = "";

        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
        Statement st = con.createStatement();

        // Check if email already exists
        String checkQuery = "SELECT COUNT(*) FROM user WHERE email = '" + email + "'";
        ResultSet rs = st.executeQuery(checkQuery);
        rs.next();
        int count = rs.getInt(1);
        
        if (count > 0) {
            JOptionPane.showMessageDialog(new JFrame(), "Email Address already exists", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Proceed with insertion
        String hashedPassword = hashPassword(password);
        String query = "INSERT INTO user(full_name, email, password) " +
                       "VALUES('" + fullName + "', '" + email + "', '" + hashedPassword + "')";

        st.execute(query);

        // Clear input fields and show success message
        fname.setText("");
        emailAddress.setText("");
        pass.setText("");
        showMessageDialog(null, "New account has been created successfully!");

    } catch (Exception e) {
        System.out.println("Error! " + e.getMessage());
        JOptionPane.showMessageDialog(new JFrame(), "An error occurred while creating your account", "Error", JOptionPane.ERROR_MESSAGE);
    }
        
    }//GEN-LAST:event_SignUpBtnActionPerformed

    // Helper method to check email format
private boolean isValidEmail(String email) {
    String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
    return email.matches(emailRegex);
}

// Helper method to check password strength
private boolean isValidPassword(String password) {
    // Check if password is at least 8 characters long and contains at least one letter and one number
    return password.length() >= 8 && 
           password.matches(".*[A-Za-z].*") && 
           password.matches(".*\\d.*") && 
           password.matches(".*[@#$%^&+=!].*");
}
    private String hashPassword(String password) {
        try {
            MessageDigest md = MessageDigest.getInstance("MD5");
            byte[] hashInBytes = md.digest(password.getBytes());
            
            // Convert bytes to hex format
            StringBuilder sb = new StringBuilder();
            for (byte b : hashInBytes) {
                sb.append(String.format("%02x", b));
            }
            return sb.toString();
        } catch (NoSuchAlgorithmException e) {
            throw new RuntimeException(e);
        }
    }
